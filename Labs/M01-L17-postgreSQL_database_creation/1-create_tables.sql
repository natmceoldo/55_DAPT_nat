CREATE TABLE IF NOT EXISTS CARS (
	"car_id" varchar,
	"vin" varchar,
	"manufacturer" varchar,
	"model" varchar,
	"car_year" int,
	"color" varchar
	);
	
CREATE TABLE IF NOT EXISTS CUSTOMERS (
	"cust_id" varchar,
	"cust_name" varchar,
	"cus_phone" varchar,
	"cust_email" varchar,
	"cust_address" varchar,
	"cust_zipcode" varchar);